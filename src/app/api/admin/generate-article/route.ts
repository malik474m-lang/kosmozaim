export async function POST(request: NextRequest) {
  const valid = await verifySession();
  if (!valid) {
    return NextResponse.json({ error: "Unauthorized" }, { status: 401 });
  }

  try {
    const { topic, category } = await request.json();

    // Выбираем тему
    let selectedTopic = topic;
    let selectedCategory = category || "займы";

    if (!selectedTopic) {
      const categoryData = topics[Math.floor(Math.random() * topics.length)];
      selectedCategory = categoryData.category;
      selectedTopic = categoryData.themes[Math.floor(Math.random() * categoryData.themes.length)];
    }

    // Пробуем AI генерацию
    let content: string | null = null;
    let excerpt: string;
    let aiProvider = "template";

    const aiPrompt = `Напиши информативную статью на тему: "${selectedTopic}".
    
Категория: ${selectedCategory}

Требования:
- Объём 800-1200 слов
- Практические советы для читателей из России
- Структура с подзаголовками
- Упомяни, что на сайте Космозайм можно сравнить предложения
- Актуальная информация для ${new Date().getFullYear()} года
- Не используй markdown, пиши обычным текстом`;

    // Пробуем YandexGPT
    if (!content && YANDEX_API_KEY) {
      content = await generateWithYandexGPT(aiPrompt);
      if (content) aiProvider = "YandexGPT";
    }

    // Пробуем GigaChat
    if (!content && GIGACHAT_AUTH) {
      content = await generateWithGigaChat(aiPrompt);
      if (content) aiProvider = "GigaChat";
    }

    // Fallback на шаблоны
    if (!content) {
      const template = generateFromTemplate(selectedTopic, selectedCategory);
      content = template.content;
      excerpt = template.excerpt;
    } else {
      excerpt = content.split("\n\n")[1]?.slice(0, 200) + "..." || content.slice(0, 200) + "...";
    }

    // Генерируем мета-данные
    const metaTitle = `${selectedTopic} | Космозайм`;
    const metaDescription = excerpt.slice(0, 155);
    const slug = slugify(selectedTopic) + "-" + Date.now();

    // Сохраняем в базу
    const newArticle = await db
      .insert(articles)
      .values({
        title: selectedTopic,
        slug,
        excerpt,
        content,
        metaTitle,
        metaDescription,
        isPublished: false,
      })
      .returning();

    return NextResponse.json({
      success: true,
      article: newArticle[0],
      aiProvider,
    });
  } catch (e) {
    console.error(e);
    return NextResponse.json({ error: "Ошибка генерации" }, { status: 500 });
  }
}

// GET - получить доступные темы и статус AI
export async function GET() {
  const valid = await verifySession();
  if (!valid) {
    return NextResponse.json({ error: "Unauthorized" }, { status: 401 });
  }

  const aiStatus = {
    yandexGPT: !!YANDEX_API_KEY && !!YANDEX_FOLDER_ID,
    gigaChat: !!GIGACHAT_AUTH,
  };

  return NextResponse.json({ 
    topics,
    aiStatus,
    hasAI: aiStatus.yandexGPT || aiStatus.gigaChat,
  });
}
