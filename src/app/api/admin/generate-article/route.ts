// Генерация через YandexGPT
async function generateWithYandexGPT(prompt: string): Promise<string | null> {
  if (!YANDEX_API_KEY || !YANDEX_FOLDER_ID) return null;

  try {
    const response = await fetch(
      "https://llm.api.cloud.yandex.net/foundationModels/v1/completion",
      {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
          Authorization: `Api-Key ${YANDEX_API_KEY}`,
          "x-folder-id": YANDEX_FOLDER_ID,
        },
        body: JSON.stringify({
          modelUri: `gpt://${YANDEX_FOLDER_ID}/yandexgpt-lite`,
          completionOptions: {
            stream: false,
            temperature: 0.6,
            maxTokens: 2000,
          },
          messages: [
            {
              role: "system",
              text: `Ты — эксперт по финансам и копирайтер для сайта Космозайм. 
Пиши информативные SEO-оптимизированные статьи на русском языке.
Используй простой и понятный язык. Добавляй практические советы.
Структурируй текст с подзаголовками. Объём: 800-1200 слов.
Не используй markdown разметку, пиши обычным текстом с переносами строк.`,
            },
            { role: "user", text: prompt },
          ],
        }),
      }
    );

    if (!response.ok) return null;

    const data = await response.json();
    return data.result?.alternatives?.[0]?.message?.text || null;
  } catch {
    return null;
  }
}

// Генерация через GigaChat (Сбер)
async function generateWithGigaChat(prompt: string): Promise<string | null> {
  if (!GIGACHAT_AUTH) return null;

  try {
    // Получаем access token
    const tokenResponse = await fetch("https://ngw.devices.sberbank.ru:9443/api/v2/oauth", {
      method: "POST",
      headers: {
        "Content-Type": "application/x-www-form-urlencoded",
        Accept: "application/json",
        RqUID: crypto.randomUUID(),
        Authorization: `Basic ${GIGACHAT_AUTH}`,
      },
      body: "scope=GIGACHAT_API_PERS",
    });

    if (!tokenResponse.ok) return null;

    const tokenData = await tokenResponse.json();
    const accessToken = tokenData.access_token;

    // Генерируем текст
    const response = await fetch("https://gigachat.devices.sberbank.ru/api/v1/chat/completions", {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
        Authorization: `Bearer ${accessToken}`,
      },
      body: JSON.stringify({
        model: "GigaChat",
        messages: [
          {
            role: "system",
            content: `Ты — эксперт по финансам и копирайтер для сайта Космозайм. 
Пиши информативные SEO-оптимизированные статьи на русском языке.
Используй простой и понятный язык. Добавляй практические советы.
Структурируй текст с подзаголовками. Объём: 800-1200 слов.`,
          },
          { role: "user", content: prompt },
        ],
        temperature: 0.7,
        max_tokens: 2000,
      }),
    });

    if (!response.ok) return null;

    const data = await response.json();
    return data.choices?.[0]?.message?.content || null;
  } catch {
    return null;
  }
}

